package pojo;

public class Location {
	private double lat;
	private double lng;
	private double png;

	public double getPng() {
		return png;
	}

	public void setPng(double png) {
		this.png = png;
	}

	public double getLat() { 
		return lat;
	}

	public void setLat(double lat) {
		this.lat = lat;
	}

	public double getLng() {
		return lng;
	}

	public void setLng(double lng) {
		this.lng = lng;
	}

}
